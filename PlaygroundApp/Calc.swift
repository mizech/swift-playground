import Foundation

class Calc {
	static func add(num1: Int, num2: Int) -> Int {
		num1 + num2;
	}
	
	static func subtract(num1: Int, num2: Int) -> Int {
		num1 - num2;
	}
	
	static func multiply(num1: Int, num2: Int) -> Int {
		num1 * num2;
	}
	
	static func divide(num1: Int, num2: Int) -> Int {
		num1 / num2;
	}
}
